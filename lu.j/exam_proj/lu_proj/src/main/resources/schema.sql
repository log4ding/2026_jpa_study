CREATE TABLE MEMBER (
    ID VARCHAR(255) NOT NULL,
    NAME VARCHAR(255),
    AGE INTEGER,
    PRIMARY KEY (ID)
);

CREATE TABLE CHAPTER5_MEMBER (
    MEMBER_ID BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    NAME VARCHAR(255),
    AGE INTEGER,
    TEAM_ID BIGINT,
    LOCKER_ID BIGINT,
    PRIMARY KEY (MEMBER_ID)
);

CREATE TABLE TEAM (
    TEAM_ID BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    NAME VARCHAR(255),
    AGE INTEGER,
    PRIMARY KEY (TEAM_ID)
);

CREATE TABLE LOCKER (
    LOCKER_ID BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    NAME VARCHAR(255),
    PRIMARY KEY (LOCKER_ID)
);

CREATE TABLE CHAPTER6_PRODUCT (
    PRODUCT_ID BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    NAME VARCHAR(255),
    PRIMARY KEY (PRODUCT_ID)
);

CREATE TABLE CHAPTER6_ORDER (
    ORDER_ID BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    NAME VARCHAR(255),
    MEMBER_ID BIGINT,
    PRODUCT_ID BIGINT,
    ORDER_COUNT INTEGER,
    ORDER_DATE DATE,
    PRIMARY KEY (ORDER_ID)
);

CREATE TABLE CHAPTER7_ITEM (
    ITEM_ID BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    NAME VARCHAR(255) NOT NULL,
    BASE_PRICE BIGINT NOT NULL,
    DTYPE VARCHAR(31),
    CREATED_AT TIMESTAMP NOT NULL,
    UPDATED_AT TIMESTAMP NOT NULL,
    PRIMARY KEY (ITEM_ID)
);

CREATE TABLE CHAPTER7_NORMAL_ITEM (
    ITEM_ID BIGINT NOT NULL,
    SKU VARCHAR(255) NOT NULL,
    STOCK_QUANTITY INTEGER NOT NULL,
    PRIMARY KEY (ITEM_ID)
);

CREATE TABLE CHAPTER7_SUBSCRIPTION_ITEM (
    ITEM_ID BIGINT NOT NULL,
    BILLING_CYCLE_DAYS INTEGER NOT NULL,
    TRIAL_DAYS INTEGER NOT NULL,
    PRIMARY KEY (ITEM_ID)
);

CREATE TABLE CHAPTER7_ORDER (
    ORDER_ID BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    ORDER_NO VARCHAR(255) NOT NULL,
    CREATED_AT TIMESTAMP NOT NULL,
    UPDATED_AT TIMESTAMP NOT NULL,
    PRIMARY KEY (ORDER_ID)
);

CREATE TABLE CHAPTER7_ORDER_ITEM (
    ORDER_ITEM_ID BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    ORDER_ID BIGINT NOT NULL,
    ITEM_ID BIGINT NOT NULL,
    ITEM_NAME_SNAPSHOT VARCHAR(255) NOT NULL,
    UNIT_PRICE_SNAPSHOT BIGINT NOT NULL,
    QUANTITY INTEGER NOT NULL,
    CREATED_AT TIMESTAMP NOT NULL,
    UPDATED_AT TIMESTAMP NOT NULL,
    PRIMARY KEY (ORDER_ITEM_ID)
);

CREATE TABLE CHAPTER7_ORDER_ITEM_OPTION (
    ORDER_ITEM_ID BIGINT NOT NULL,
    OPTION_KEY VARCHAR(255) NOT NULL,
    OPTION_VALUE VARCHAR(255) NOT NULL
);

CREATE UNIQUE INDEX UK_CHAPTER7_ORDER_NO ON CHAPTER7_ORDER (ORDER_NO);

